-- LeatherTrace-BD Online v3.0
-- Run this complete script once in Supabase Dashboard -> SQL Editor.
-- It creates the cloud database, individual identities, role/access metadata,
-- image and Excel storage buckets, views, and an atomic SHA-256 hash-linked ledger.

create extension if not exists pgcrypto;

create table if not exists public.app_users (
    id bigint generated by default as identity primary key,
    individual_user_id text not null unique,
    username text not null unique,
    full_name text not null,
    password_salt text not null,
    password_hash text not null,
    role text not null check (role in (
        'collector','tannery_intake','quality_inspector','environmental_officer',
        'tannery_processing','buyer','auditor','admin'
    )),
    role_code text not null,
    access_profile text not null,
    organisation text not null,
    active boolean not null default true,
    must_change_password boolean not null default true,
    password_changed_at timestamptz,
    created_at timestamptz not null default now()
);

