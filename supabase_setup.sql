create or replace function public.append_ledger_transaction(
    p_transaction_id text,
    p_record_type text,
    p_related_batch_id text,
    p_actor_user_id bigint,
    p_actor_individual_id text,
    p_actor_role text,
    p_actor_org text,
    p_payload jsonb
) returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
    v_previous_hash text;
    v_record_hash text;
    v_created_at timestamptz := clock_timestamp();
    v_canonical text;
    v_row public.ledger_transactions%rowtype;
begin
    perform pg_advisory_xact_lock(7823419901);
    select record_hash into v_previous_hash from public.ledger_transactions order by id desc limit 1;
    v_previous_hash := coalesce(v_previous_hash, repeat('0', 64));
    v_canonical := jsonb_build_object(
        'transaction_id', p_transaction_id,
        'record_type', p_record_type,
        'related_batch_id', p_related_batch_id,
        'actor_user_id', p_actor_user_id,
        'actor_individual_id', p_actor_individual_id,
        'actor_role', p_actor_role,
        'actor_org', p_actor_org,
        'payload', p_payload,
        'previous_hash', v_previous_hash,
        'created_at', v_created_at
    )::text;
    v_record_hash := encode(digest(v_canonical, 'sha256'), 'hex');
    insert into public.ledger_transactions(
        transaction_id, record_type, related_batch_id, actor_user_id,
        actor_individual_id, actor_role, actor_org, payload, previous_hash, record_hash, created_at
    ) values (
        p_transaction_id, p_record_type, p_related_batch_id, p_actor_user_id,
        p_actor_individual_id, p_actor_role, p_actor_org, p_payload, v_previous_hash, v_record_hash, v_created_at
    ) returning * into v_row;
    return to_jsonb(v_row);
end;
$$;

create or replace function public.verify_ledger()
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
    r public.ledger_transactions%rowtype;
    v_expected_previous text := repeat('0', 64);
    v_expected_hash text;
    v_canonical text;
    v_checked integer := 0;
begin
    for r in select * from public.ledger_transactions order by id loop
        if r.previous_hash <> v_expected_previous then
            return jsonb_build_object('valid', false, 'checked', v_checked, 'invalid_id', r.id,
                                      'message', 'Previous-hash linkage mismatch');
        end if;
        v_canonical := jsonb_build_object(
            'transaction_id', r.transaction_id,
            'record_type', r.record_type,
            'related_batch_id', r.related_batch_id,
            'actor_user_id', r.actor_user_id,
            'actor_individual_id', r.actor_individual_id,
            'actor_role', r.actor_role,
            'actor_org', r.actor_org,
            'payload', r.payload,
            'previous_hash', r.previous_hash,
            'created_at', r.created_at
        )::text;
        v_expected_hash := encode(digest(v_canonical, 'sha256'), 'hex');
        if r.record_hash <> v_expected_hash then
            return jsonb_build_object('valid', false, 'checked', v_checked, 'invalid_id', r.id,
                                      'message', 'Record hash mismatch');
        end if;
        v_expected_previous := r.record_hash;
        v_checked := v_checked + 1;
    end loop;
    return jsonb_build_object('valid', true, 'checked', v_checked, 'invalid_id', null,
                              'message', 'Ledger verified successfully');
end;
$$;

-- Views used by the application and the linked Excel workbook.
create or replace view public.source_batch_view as
select b.id, b.batch_id, b.collector_user_id, u.individual_user_id as collector_individual_id,
       u.organisation as collector_organisation, b.hide_count, b.status, b.row_version,
       b.created_at, b.updated_at,
       s.source_receipt_id, s.source_type, s.source_region, s.reference_status,
       s.official_reference, s.collection_timestamp, s.gps_lat, s.gps_lon,
       s.formatted_address, s.google_place_id, s.google_maps_url, s.gps_capture_method,
       s.slaughter_point_type, s.preservation_method, s.salting_delay_hours, s.notes,
       i.inspection_id, i.image_storage_path, i.original_filename, i.image_sha256,
       i.image_uploaded_at, i.ai_mode, i.detected_defects, i.defect_score,
       i.defective_area_pct, i.usable_area_pct, i.ai_suggested_grade, i.ai_confidence,
       i.human_confirmed_grade, i.status as inspection_status
from public.batches b
join public.source_receipts s on s.id=b.source_receipt_id
left join public.app_users u on u.id=b.collector_user_id
left join public.inspections i on i.batch_id=b.id;

create or replace view public.tannery_receive_view as
select * from public.source_batch_view;

create or replace view public.inspection_view as
select i.id, i.inspection_id, i.batch_id as batch_id_db, b.batch_id,
       i.image_storage_path, i.original_filename, i.image_sha256, i.image_uploaded_at,
       i.ai_mode, i.detected_defects, i.defect_score, i.defective_area_pct,
       i.usable_area_pct, i.ai_suggested_grade, i.ai_confidence,
       i.human_confirmed_grade, i.override_reason, i.status, i.created_at, i.reviewed_at,
       s.source_region, s.collection_timestamp, s.gps_lat, s.gps_lon, s.formatted_address
from public.inspections i
join public.batches b on b.id=i.batch_id
join public.source_receipts s on s.id=b.source_receipt_id;

create or replace view public.processing_candidate_view as
select b.id, b.batch_id, b.status, s.reference_status, s.salting_delay_hours,
       i.ai_suggested_grade, i.human_confirmed_grade, i.defect_score
from public.batches b
join public.source_receipts s on s.id=b.source_receipt_id
join public.inspections i on i.batch_id=b.id and i.status='CONFIRMED'
left join public.processing_lots p on p.batch_id=b.id
where p.id is null;

create or replace view public.processing_view as
select p.id, p.processing_lot_id, p.batch_id as batch_id_db, b.batch_id,
       p.production_shift_id, p.environmental_record_id, p.process_stage,
       p.process_start, p.process_end, p.output_area_sqft, p.created_at,
       e.effluent_record_id, e.compliance_status,
       d.decision_id, d.final_decision, d.payment_action, d.reasons
from public.processing_lots p
join public.batches b on b.id=p.batch_id
left join public.environmental_records e on e.id=p.environmental_record_id
left join public.decisions d on d.batch_id=p.batch_id;

create or replace view public.decision_view as
select d.id, d.decision_id, b.batch_id, d.final_decision, d.payment_action,
       d.quality_status, d.environmental_status, d.integrity_status,
       d.reasons, d.created_at
from public.decisions d join public.batches b on b.id=d.batch_id;

create or replace view public.traceability_view as
select f.id, f.finished_lot_id, f.traceability_qr_id, f.destination_market,
       f.buyer_reference, f.status as traceability_status, f.created_at,
       p.processing_lot_id, p.production_shift_id, p.process_stage,
       b.batch_id, s.source_type, s.source_region, s.collection_timestamp,
       s.gps_lat, s.gps_lon, s.formatted_address, s.google_maps_url,
       i.image_storage_path, i.image_sha256, i.image_uploaded_at,
       i.ai_suggested_grade, i.ai_confidence, i.human_confirmed_grade,
       e.compliance_status, d.final_decision, d.payment_action, d.integrity_status
from public.finished_lots f
join public.processing_lots p on p.id=f.processing_lot_id
join public.batches b on b.id=p.batch_id
join public.source_receipts s on s.id=b.source_receipt_id
join public.inspections i on i.batch_id=b.id
left join public.environmental_records e on e.id=p.environmental_record_id
left join public.decisions d on d.batch_id=b.id;

